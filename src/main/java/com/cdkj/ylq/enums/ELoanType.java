public enum ELoanType {
    BAOFOO("1", "宝付代付"), OFFLINE("2", "人工打款");

    ELoanType(String code, String value) {
        this.code = code;
        this.value = value;
    }

    private String code;

    private String value;

    public String getCode() {
        return code;
    }

    public String getValue() {
        return value;
    }
}
